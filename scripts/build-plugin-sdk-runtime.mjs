import path from 'node:path'
import { pathToFileURL } from 'node:url'
import { build } from 'vite'

const defaultWorkspaceRoot = path.resolve(import.meta.dirname, '..')
const GENERATED_BANNER = '/* Generated by pnpm build:plugin-sdk-runtime from packages/plugin-sdk/src/index.ts. Do not edit. */'

export async function buildPluginSdkRuntime(options = {}) {
  const workspaceRoot = options.workspaceRoot ? path.resolve(options.workspaceRoot) : defaultWorkspaceRoot
  const outDir = options.outDir
    ? path.resolve(options.outDir)
    : path.join(workspaceRoot, 'src-tauri/plugin-host/plugin-sdk')
  const entry = path.join(workspaceRoot, 'packages/plugin-sdk/src/index.ts')

  await build({
    configFile: false,
    root: workspaceRoot,
    publicDir: false,
    logLevel: options.logLevel ?? 'info',
    build: {
      emptyOutDir: true,
      lib: {
        entry,
        formats: ['es'],
        fileName: () => 'index.js',
      },
      minify: false,
      outDir,
      sourcemap: false,
      target: 'es2022',
      rollupOptions: {
        output: {
          banner: GENERATED_BANNER,
        },
      },
    },
  })

  return path.join(outDir, 'index.js')
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  await buildPluginSdkRuntime()
}
