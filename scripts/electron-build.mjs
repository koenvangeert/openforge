#!/usr/bin/env node
import { cp, mkdir, rm, writeFile } from 'node:fs/promises'
import { spawn } from 'node:child_process'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { build as viteBuild } from 'vite'
import { buildPluginSdkRuntime } from './build-plugin-sdk-runtime.mjs'

function repoRoot() {
  return resolve(dirname(fileURLToPath(import.meta.url)), '..')
}

export const HOST_RUNTIME_RESOURCE_DIR = 'plugin-host'
const BACKEND_PLUGIN_HOST_BANNER = '/* Generated by pnpm electron:build from src-tauri/plugin-host/index.ts. Do not edit. */'

export async function buildBackendPluginHostRuntime(root = repoRoot(), outDir = resolve(root, 'dist-electron', HOST_RUNTIME_RESOURCE_DIR)) {
  await viteBuild({
    configFile: false,
    root,
    publicDir: false,
    logLevel: 'info',
    resolve: {
      alias: {
        '@openforge/plugin-runtime/commandValidation': resolve(root, 'packages', 'plugin-runtime', 'src', 'commandValidation.ts'),
        '@openforge/plugin-runtime': resolve(root, 'packages', 'plugin-runtime', 'src', 'index.ts'),
      },
    },
    build: {
      emptyOutDir: false,
      lib: {
        entry: resolve(root, 'src-tauri', 'plugin-host', 'index.ts'),
        formats: ['es'],
        fileName: () => 'index.js',
      },
      minify: false,
      outDir,
      sourcemap: false,
      target: 'es2022',
      rollupOptions: {
        external: id => id.startsWith('node:'),
        output: {
          banner: BACKEND_PLUGIN_HOST_BANNER,
        },
      },
    },
  })

  return resolve(outDir, 'index.js')
}

export async function copyHostRuntimeAssets(root = repoRoot(), electronDist = resolve(root, 'dist-electron')) {
  const hostRuntimeDir = resolve(electronDist, HOST_RUNTIME_RESOURCE_DIR)
  await rm(hostRuntimeDir, { recursive: true, force: true })
  await mkdir(hostRuntimeDir, { recursive: true })
  await buildPluginSdkRuntime({
    workspaceRoot: root,
    outDir: resolve(hostRuntimeDir, 'plugin-sdk'),
  })
  await buildBackendPluginHostRuntime(root, hostRuntimeDir)

  const svelteRuntimeDir = resolve(hostRuntimeDir, 'svelte')
  await cp(resolve(root, 'node_modules', 'svelte', 'src'), svelteRuntimeDir, { recursive: true })
  await writeSvelteHostRuntimeAliases(svelteRuntimeDir)
}

async function writeSvelteHostRuntimeAliases(svelteRuntimeDir) {
  await mkdir(svelteRuntimeDir, { recursive: true })
  await Promise.all([
    writeFile(resolve(svelteRuntimeDir, 'index.js'), "export * from './index-client.js';\n"),
    writeFile(resolve(svelteRuntimeDir, 'internal.js'), "export * from './internal/index.js';\n"),
    writeFile(resolve(svelteRuntimeDir, 'store.js'), "export * from './store/index-client.js';\n"),
  ])
}

function waitForExit(child, label) {
  return new Promise((resolvePromise, reject) => {
    child.once('error', reject)
    child.once('exit', (code, signal) => {
      if (code === 0) {
        resolvePromise()
        return
      }
      reject(new Error(`${label} exited with ${signal ?? `code ${code}`}`))
    })
  })
}

async function main() {
  await rm(resolve(repoRoot(), 'dist-electron'), { recursive: true, force: true })
  const root = repoRoot()
  await waitForExit(spawn('tsc', ['-p', 'tsconfig.electron.json'], {
    cwd: root,
    stdio: 'inherit',
    shell: process.platform === 'win32',
  }), 'tsc -p tsconfig.electron.json')
  await copyHostRuntimeAssets(root)
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main().catch(error => {
    console.error(error instanceof Error ? error.message : error)
    process.exit(1)
  })
}
